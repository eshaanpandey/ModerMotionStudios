export const getAllProjects = (): Promise<Project[]> => {
    return new Promise((resolve) => {
        setTimeout(() => resolve(projects), 500); // Simulate network delay
    });
};

export const getProjectsByCategory = (category: string): Promise<Project[]> => {
    return new Promise((resolve) => {
        setTimeout(() => resolve(projects.filter((p) => p.category === category)), 500);
    });
};

export const getProjectById = (id: string): Promise<Project | undefined> => {
    return new Promise((resolve) => {
        setTimeout(() => resolve(projects.find((p) => p.id === id)), 500);
    });
};

export const fetchMoreData = async (page: number): Promise<Project[]> => {
    const pageSize = 10; // Load 10 projects per page.
    const startIndex = (page - 1) * pageSize;
    const endIndex = page * pageSize;
  
    return new Promise((resolve) => {
      setTimeout(() => {
        resolve(projects.slice(startIndex, endIndex));
      }, 500);
    });
  };

